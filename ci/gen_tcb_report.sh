echo "| Runtime_Trace_refines | assumption (tcb-assumption) | event log in order | diff_trace (3000) |"
echo "| Cache (CGet/CPut) | effect | Rkv.Cache | diff_cache |"
echo "| Runtime_Cache_invisible | assumption (tcb-assumption) | hit == miss (invisible) | diff_cache (3000) |"
echo "| Codec | typed-witness realizer (GADT, no casts) | Rkv.Codec | codec_test; proven in Codec.v |"
echo
echo "## Public effectful entrypoints"
echo "- \`Generated.Prog0_generated.prog0\` under \`Rkv.Kv.run\`/\`run_checked\` (unhandled -> typed error, T8)."
echo "- error programs (sample_throw/sample_guard5) under \`Err.run_error (Kv.run …)\` (throw -> Error e, aborts)."
echo
echo "## Forbidden-API gates (CI)"
echo "- no Obj.magic; Effect.perform confined to runtime/; no Admitted/admit/Axiom; no unregistered Extract Constant; generated file unedited."
echo "- codegen wf-gates every program pre-emission with the EXTRACTED, PROVEN well-formedness checker (theories/Wf.v wf_tm, adr-0014 — scope/arity/binder counts; not a new realizer, no opt-out; ill-formed input fails the run loudly)."
} > "$out"
echo "wrote $out"
